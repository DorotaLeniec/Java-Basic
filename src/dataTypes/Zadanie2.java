public class Zadanie2 {
    public static void main(String[] args){
        int a = 2+3;
        System.out.println("2+3 " + a);

        int b = 2-4;
        System.out.println("2-4 " + b);

        int d = 5/2;
        System.out.println("5/2 " + d);

        double c = 5.0/2;
        System.out.println("5.0/2 " + c);

        double e = 5.0/2.0;
        System.out.println("5.0/2.0 " + e);

        long f = 100L -10;
        System.out.println("100L -10 " + f);

        float g = 2f-3;
        System.out.println("2f-3 " +g);

        float h = 5f/2;
        System.out.println("5f/2 "+h);

        double j = 5d/2;
        System.out.println("5d/2 " + j);

        int k = 'A' + 2;
        System.out.println("'A' + 2 " + k);

        int l = 'a'+ 2;
        System.out.println("'a'+2 " + l);

        String m = "a" + 2;
        System.out.println("\"a\"+2 " + m);

        String o = "a" + "b";
        System.out.println("\"a\"+\"b\" " + o);

        int p = 'a' + 'b';
        System.out.println("'a' + 'b' " + p);

        String r = "a" + 'b';
        System.out.println("\"a\" + 'b' " + r);

        String s = "a" + 'b' + 3;
        System.out.println("\"a\" + 'b' + 3 " + s);

        String t = 'b' + 3 + "a";
        System.out.println("'b' + 3 + \"a\" " +t);

        int w = 9%4;
        System.out.println("9%4 " +w);

        System.out.println(3 - 2);


    }
}
